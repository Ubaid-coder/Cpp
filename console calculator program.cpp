#include <iostream>

int main(){

    char op;
    double num1, num2, result;

    std::cout << "********** CALCULATOR *********** \n";

    std::cout << "Enter either ( + - * /): ";
    std::cin >> op;

    std::cout << "Enter number1: ";
    std::cin >> num1;

    std::cout << "Enter number2: ";
    std::cin >> num2;

    switch(op){
        case '+':
            result = num1 + num2;
            break;
        case '-':
            result = num1 - num2;
        case '*':
            result = num1 * num2;
            break;
        case '/':
            result = num1 / num2;
            break;

        default:
            std::cout << "Invalid operator! ";
            result = 0;
    }

    std::cout << "Result Is: " << result << '\n';

    std::cout << "*************************************";
 
    return 0;
}